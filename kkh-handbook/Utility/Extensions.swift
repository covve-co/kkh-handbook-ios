import Foundation
import UIKit

public extension UIColor {
	func secondaryColor() -> UIColor{
		return UIColor(red:0.03, green:0.38, blue:0.59, alpha:1.0)
		
	}
	func primaryColor() -> UIColor{
		return UIColor(red:0.31, green:0.57, blue:0.82, alpha:1.0)
		
	}
	
}
